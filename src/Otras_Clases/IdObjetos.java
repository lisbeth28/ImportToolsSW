package Otras_Clases;

public class IdObjetos {
    private int IdObjetos; 
    private String Referencia;
    
    public IdObjetos() { }

    /**
     * @return the IdObjetos
     */
    public int getIdObjetos() {
        return IdObjetos;
    }

    /**
     * @param IdObjetos the IdObjetos to set
     */
    public void setIdObjetos(int IdObjetos) {
        this.IdObjetos = IdObjetos;
    }

    /**
     * @return the Referencia
     */
    public String getReferencia() {
        return Referencia;
    }

    /**
     * @param Referencia the Referencia to set
     */
    public void setReferencia(String Referencia) {
        this.Referencia = Referencia;
    }
 
   

}
